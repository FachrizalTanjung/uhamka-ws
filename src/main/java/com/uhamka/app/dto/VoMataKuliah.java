package com.uhamka.app.dto;

public class VoMataKuliah {

	private Integer idMatkul;
	private String jamNgajar;
	private String namaDosen;
	private String mataKuliah;
	private String ruanganNgajar;
	private String hari;

	public Integer getIdMatkul() {
		return idMatkul;
	}

	public void setIdMatkul(Integer idMatkul) {
		this.idMatkul = idMatkul;
	}

	public String getJamNgajar() {
		return jamNgajar;
	}

	public void setJamNgajar(String jamNgajar) {
		this.jamNgajar = jamNgajar;
	}

	public String getNamaDosen() {
		return namaDosen;
	}

	public void setNamaDosen(String namaDosen) {
		this.namaDosen = namaDosen;
	}

	public String getMataKuliah() {
		return mataKuliah;
	}

	public void setMataKuliah(String mataKuliah) {
		this.mataKuliah = mataKuliah;
	}

	public String getRuanganNgajar() {
		return ruanganNgajar;
	}

	public void setRuanganNgajar(String ruanganNgajar) {
		this.ruanganNgajar = ruanganNgajar;
	}

	public String getHari() {
		return hari;
	}

	public void setHari(String hari) {
		this.hari = hari;
	}

}
