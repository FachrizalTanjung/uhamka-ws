package com.uhamka.app.dto;

public class VoMahasiswa {

	private String nim;
	private String nama;
	private String tempatTglLahir;
	private String alamat;
	private String angkatan;

	public String getNim() {
		return nim;
	}

	public void setNim(String nim) {
		this.nim = nim;
	}

	public String getNama() {
		return nama;
	}

	public void setNama(String nama) {
		this.nama = nama;
	}

	public String getTempatTglLahir() {
		return tempatTglLahir;
	}

	public void setTempatTglLahir(String tempatTglLahir) {
		this.tempatTglLahir = tempatTglLahir;
	}

	public String getAlamat() {
		return alamat;
	}

	public void setAlamat(String alamat) {
		this.alamat = alamat;
	}

	public String getAngkatan() {
		return angkatan;
	}

	public void setAngkatan(String angkatan) {
		this.angkatan = angkatan;
	}

}
